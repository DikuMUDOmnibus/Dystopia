evolves, you will have a total of 9 evolves once you are fully evolved. You can combine
these 9 evolves in 512 different ways - so it's basicly up to you to design your
class, and it's highly unlikely that anyone else will pick the same set of evolves
as you, making you a very unique person.
</p>

