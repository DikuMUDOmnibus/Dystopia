opposes the tree next to it, and once you start gaining evolves in a certain tree,
the tree next to it is no longer available to you. Each evolve opposes the evolve
next to it in it's given tree, and once you have gained a certain evolve, you
cannot gain the evolve next to it in that tree.
</p>

<p>
Since there are 6 trees, of which you can take 3, and each tree allows you to pick 3
evolves, you will have a total of 9 evolves once you are fully evolved. You can combine
these 9 evolves in 512 different ways - so it's basicly up to you to design your
class, and it's highly unlikely that anyone else will pick the same set of evolves
as you, making you a very unique person.
</p>

